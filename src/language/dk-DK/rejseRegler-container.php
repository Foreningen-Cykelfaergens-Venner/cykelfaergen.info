        <h2>Rejser til Tyskland</h2>
        <p>Cykelfærgen tilbyder rejser over grænsen til Langballigau og Flensborg i tyskland. For at kunne sejle med os til Tyskland skal du huske på nogle ting. På vores skib må du have en hund med, dog med de rigtige dokumenter.</p>
        <p>
            Husk på:
            <ul>
                <li>Gyldigt pas</li>
                <li>Gyldige dokumentater til dyr</li>
            </ul>
        </p>
