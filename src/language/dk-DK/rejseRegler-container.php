        <h2>Corona</h2>
        <p>Vi er altid bestræbet om at vores gæster føgler sig trykt på deres rejser, med os på Flensborg fjord. Derfor holder vi os til al gældende Corona regler hvis vi sejler.</p>
        <p>For at du må sejler over grænsen til tyskland skal du have en <strong>negativ coronatest</strong> der er maks <strong>72 timer</strong> gammel.</p>
